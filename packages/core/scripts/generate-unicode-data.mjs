/**
 * Regenerates src/unicode/casefold-data.ts and src/unicode/whitespace-data.ts
 * from the Unicode Character Database. Not run as part of build/test; run by hand
 * when bumping the vendored Unicode version. See docs/unicode-normalization.md.
 *
 * Usage (from packages/core/scripts/):
 *   curl -O https://www.unicode.org/Public/<version>/ucd/CaseFolding.txt
 *   curl -O https://www.unicode.org/Public/<version>/ucd/PropList.txt
 *   node generate-unicode-data.mjs
 *   mv out.casefold-data.ts ../src/unicode/casefold-data.ts
 *   mv out.whitespace-data.ts ../src/unicode/whitespace-data.ts
 *   rm CaseFolding.txt PropList.txt
 */
import { readFileSync, writeFileSync } from 'node:fs';

const caseFoldingRaw = readFileSync('./CaseFolding.txt', 'utf-8');
const propListRaw = readFileSync('./PropList.txt', 'utf-8');

// ---- CaseFolding: keep only C (common) and F (full) statuses ----
const foldEntries = [];
for (const line of caseFoldingRaw.split('\n')) {
  const trimmed = line.split('#')[0].trim();
  if (!trimmed) continue;
  const parts = trimmed.split(';').map(p => p.trim());
  if (parts.length < 3) continue;
  const [codeHex, status, mappingHex] = parts;
  if (status !== 'C' && status !== 'F') continue;
  const code = parseInt(codeHex, 16);
  const mapping = mappingHex.split(/\s+/).filter(Boolean).map(h => parseInt(h, 16));
  foldEntries.push([code, mapping]);
}
foldEntries.sort((a, b) => a[0] - b[0]);

// ---- PropList: White_Space ranges (exclude Pattern_White_Space) ----
const wsRanges = [];
for (const line of propListRaw.split('\n')) {
  const withoutComment = line.split('#')[0].trim();
  if (!withoutComment) continue;
  const m = withoutComment.match(/^([0-9A-Fa-f]{4,6})(?:\.\.([0-9A-Fa-f]{4,6}))?\s*;\s*(\S+)$/);
  if (!m) continue;
  const [, startHex, endHex, prop] = m;
  if (prop !== 'White_Space') continue;
  const start = parseInt(startHex, 16);
  const end = endHex ? parseInt(endHex, 16) : start;
  wsRanges.push([start, end]);
}
wsRanges.sort((a, b) => a[0] - b[0]);

// ---- Emit casefold-data.ts ----
const foldLines = foldEntries
  .map(([code, mapping]) => `[0x${code.toString(16).toUpperCase()},[${mapping.map(c => '0x' + c.toString(16).toUpperCase()).join(',')}]]`)
  .join(',\n  ');

const casefoldTs = `/**
 * Vendored Unicode 15.0.0 full case-fold mapping.
 * Source: https://www.unicode.org/Public/15.0.0/ucd/CaseFolding.txt (generated 2022-02-02)
 * Includes only status C (common) and F (full) mappings, per the Unicode default
 * case-insensitive matching algorithm. Simple-only (S) and Turkic (T) mappings are
 * intentionally excluded.
 *
 * Generated by packages/core/scripts/generate-unicode-data.mjs — see that script's
 * header and docs/unicode-normalization.md for regeneration instructions.
 */

// Map from source code point to an array of target code points (full case fold
// mappings may expand into more than one code point, e.g. U+00DF -> "ss").
export const CASEFOLD_DATA: ReadonlyArray<readonly [number, readonly number[]]> = [
  ${foldLines}
];

export const CASEFOLD_UNICODE_VERSION = '15.0.0';
`;

writeFileSync('./out.casefold-data.ts', casefoldTs, 'utf-8');

// ---- Emit whitespace-data.ts ----
const wsLines = wsRanges
  .map(([start, end]) => `[0x${start.toString(16).toUpperCase()},0x${end.toString(16).toUpperCase()}]`)
  .join(', ');

const whitespaceTs = `/**
 * Vendored Unicode 15.0.0 White_Space property ranges.
 * Source: https://www.unicode.org/Public/15.0.0/ucd/PropList.txt, White_Space entries only
 * (Pattern_White_Space entries are excluded; they are a distinct, narrower property).
 *
 * Generated by packages/core/scripts/generate-unicode-data.mjs — see that script's
 * header and docs/unicode-normalization.md for regeneration instructions.
 */

// Inclusive [start, end] code point ranges, sorted ascending, non-overlapping.
export const WHITE_SPACE_RANGES: ReadonlyArray<readonly [number, number]> = [
  ${wsLines}
];

export const WHITE_SPACE_UNICODE_VERSION = '15.0.0';
`;

writeFileSync('./out.whitespace-data.ts', whitespaceTs, 'utf-8');

console.log('fold entries:', foldEntries.length, 'ws ranges:', wsRanges.length);
